package a;

public class Escape {
    public static void main(String[] args) {
        System.out.println("Esca\tpe");
        System.out.println("Esca\bpe");
        System.out.println("Esca\npe");
        System.out.println("Esca\rpe");
        System.out.println("Esca\'pe");
        System.out.println("Esca\"pe");
        System.out.println("Esca\\pe");
    }

}
